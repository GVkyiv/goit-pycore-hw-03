import re  # Модуль для роботи з регулярними виразами

# Функція нормалізує номер телефону до єдиного формату +380XXXXXXXXX
def normalize_phone(phone_number: str) -> str:
    # Видаляємо всі символи, крім цифр і знака "+"
    cleaned = re.sub(r"[^\d+]", "", phone_number).strip()

    # Якщо номер вже починається з "+"
    if cleaned.startswith("+"):
        # Залишаємо тільки цифри
        digits = re.sub(r"\D", "", cleaned)
        # Повертаємо номер у форматі +<цифри>
        return f"+{digits}"

    # Якщо "+" немає — залишаємо тільки цифри
    digits = re.sub(r"\D", "", cleaned)

    # Якщо номер починається з коду країни 380
    if digits.startswith("380"):
        # Просто додаємо "+"
        return f"+{digits}"

    # В іншому випадку вважаємо, що це локальний номер
    # і додаємо код країни України +38
    return f"+38{digits}"


# Цей блок виконується лише при запуску файлу напряму
if __name__ == "__main__":
    # Початкові "сирі" номери телефонів у різних форматах
    raw_numbers = [
        "067\t123 4567",
        "(095) 234-5678\n",
        "+380 44 123 4567",
        "380501234567",
        "    +38(050)123-32-34",
        "     0503451234",
        "(050)8889900",
        "38050-111-22-22",
        "38050 111 22 11   ",
    ]

    # Нормалізуємо кожен номер зі списку за допомогою list comprehension
    sanitized_numbers = [normalize_phone(num) for num in raw_numbers]

    # Виводимо результат — готові номери для SMS-розсилки
    print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)

